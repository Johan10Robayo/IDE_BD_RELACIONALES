CREATE OR REPLACE FUNCTION NUM_STUDENTS(ID_SUBJECT NUMERIC, ID_PROGRAM NUMERIC, ID_FACULTY NUMERIC, ACT_YEAR NUMERIC, SEMESTER NUMERIC) RETURNS NUMERIC AS $$
DECLARE
NUM_STUNDENTS NUMERIC:=0;
     MY_STUDENTS CURSOR FOR 
        SELECT COUNT(*) AS NUM_ESTUDIANTES
        FROM INSCRIPCIONES INS
        WHERE INS.MATERIA=ID_SUBJECT AND INS.PROGRAMA=ID_PROGRAM AND
        INS.FACULTAD=ID_FACULTY AND INS.ANO=ACT_YEAR AND INS.PERIODO=SEMESTER
        GROUP BY INS.MATERIA;
    C_STUDENTS RECORD;

BEGIN 
    FOR C_STUDENTS IN MY_STUDENTS LOOP
        NUM_STUNDENTS:=C_STUDENTS.NUM_ESTUDIANTES;
    END LOOP;
RETURN NUM_STUNDENTS;

END;


$$ LANGUAGE plpgsql;
