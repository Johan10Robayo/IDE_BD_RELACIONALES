CREATE OR REPLACE FUNCTION PROMEDIO_EST (COD NUMERIC) RETURNS NUMERIC AS $$
DECLARE
NOTA NUMERIC;
AUX NUMERIC;
BEGIN
    SELECT E.CODIGO, ROUND(AVG(I.NOTA), 2) INTO AUX, NOTA
    FROM ESTUDIANTE E
    JOIN INSCRIPCIONES I ON E.CODIGO = I.ESTUDIANTE
    WHERE E.CODIGO = COD AND I.NOTA >= 3
