CREATE TABLE EMPLEADOS(
    IDENTIFICACION NUMERIC NOT NULL,
    TIPO CHARACTER VARYING(5) NOT NULL,
    NOMBRE_1 CHARACTER VARYING(50),
    NOMBRE_2 CHARACTER VARYING(50),
    APELLIDO_1 CHARACTER VARYING(50),
    APELLIDO_2 CHARACTER VARYING(50),
    SEXO CHARACTER VARYING(1),
    FECHA_N DATE,
    LUGAR_N CHARACTER VARYING(100),
    DIRECCION CHARACTER VARYING(50),
    TELEFONO CHARACTER VARYING(50),
    EMAIL CHARACTER VARYING(100),
    SALARIO NUMERIC,
    ACTIVO CHARACTER VARYING(1) NOT NULL,
    CONSTRAINT EMPLEADOS_PK PRIMARY KEY(IDENTIFICACION),
    CONSTRAINT EM_CHECK_TIPO CHECK (UPPER(TIPO) IN ('CC', 'TI', 'CE', 'NIT')),
    CONSTRAINT EM_CHECK_SEXO CHECK (UPPER(SEXO) IN ('M', 'F', 'O')),
    CONSTRAINT EM_CHECK_ACTIVO CHECK (UPPER(ACTIVO) IN ('S', 'N'))
);

CREATE TABLE ELEMENTOS(
    CODIGO Numeric NOT NULL,
    ELEMENTO character varying(50) NOT NULL,
    DEVOLUTIVO character varying(2),
    TALLA character varying(2),
    USO character varying(12),
    MATERIALES character varying(300),
    MANTENIMIENTO character varying(300),
    USOS character varying(300),
    NORMA character varying(350),
    ATENUACION character varying(50),
    SERIAL character varying(250),
    TALLAS character varying(250),
    UNIDAD NUMERIC,
    RUTA character varying(800),
    CONSTRAINT ELEMENTOS_PK PRIMARY KEY(CODIGO),
    CONSTRAINT EL_CHECK_DEVOLUTIVO CHECK (UPPER(DEVOLUTIVO) IN ('SI', 'NO')),
    CONSTRAINT EL_CHECK_TALLA CHECK (UPPER(TALLA) IN ('SI', 'NO'))
);

CREATE TABLE ENTREGA_ELEMENTOS(
    ID_ENTREGA NUMERIC NOT NULL,
    ELEMENTO NUMERIC NOT NULL,
    VIGENTE CHARACTER VARYING(3),
    NUMERO CHARACTER VARYING(15),
    DEVOLUTIVO CHARACTER VARYING(2),
    FECHA_DEVOLUCION DATE,
    EMPLEADO NUMERIC NOT NULL,
    FECHA DATE,
    CONSTRAINT ENTREGA_ELEMENTOS_PK PRIMARY KEY(ID_ENTREGA, ELEMENTO, EMPLEADO),
    CONSTRAINT EE_ELEMENTO_FK FOREIGN KEY (ELEMENTO) REFERENCES ELEMENTOS (CODIGO),
    CONSTRAINT EE_EMPLEADO_FK FOREIGN KEY (EMPLEADO) REFERENCES EMPLEADOS (IDENTIFICACION),
    CONSTRAINT EE_CHECK_VIGENTE CHECK(UPPER(VIGENTE) IN ('S', 'N')),
    CONSTRAINT EE_CHECK_DEVOLUTIVO CHECK(UPPER(DEVOLUTIVO) IN ('S', 'N'))
);

