CREATE OR REPLACE FUNCTION public.entrega_ele(ide numeric)
 RETURNS numeric
 LANGUAGE plpgsql
AS $function$
DECLARE 
 	empleados CURSOR  FOR
    SELECT E.IDENTIFICACION
    FROM EMPLEADOS E JOIN ELEMENTOS_ASIGNADOS EA ON E.IDENTIFICACION = EA.EMPLEADO JOIN ELEMENTOS EL ON EA.ELEMENTO = EL.CODIGO
    LEFT JOIN ENTREGA_ELEMENTOS EE ON EL.CODIGO = EE.ELEMENTO 
    WHERE EE.ELEMENTO IS NULL
    ORDER BY E.IDENTIFICACION;
	
AUX NUMERIC DEFAULT 0;
BEGIN
     
    FOR CM IN empleados LOOP
        IF CM.IDENTIFICACION = IDE THEN
            RETURN 1;
        END IF;
    END LOOP;
    
    RETURN 0;

END;
